    private void GenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerarActionPerformed
        int n = Integer.parseInt(vectorN.getText());
        int m = Integer.parseInt(vectorM.getText());
        
        //aplicando el if antes de algo, se aprendio en la clase anterior

        if (n <= 0 || m <= 0) {
            JOptionPane.showMessageDialog(this, "0 no sirve, uso otro numerito");
            return;
        }

        int[] vectorN = generateRandomVector(n);
        int[] vectorM = generateRandomVector(m);

        Area1.setText("Vector N: " + Arrays.toString(vectorN) + "\n");
        Area1.append("Vector M: " + Arrays.toString(vectorM) + "\n");

        int maxElementN = findMax(vectorN);
        int maxElementM = findMax(vectorM);
        int maxElement = Math.max(maxElementN, maxElementM);
        nmayor.setText(String.valueOf(maxElement));

        int[] invertedN = invertVector(vectorN);
        ninvertido.setText(Arrays.toString(invertedN));

        int[] evenElements = findEvenElements(vectorN);
        npares.setText(Arrays.toString(evenElements));
        
        int[] sumaVectores = sumaVectores (vectorN, vectorM);
        Area2.setText(Arrays.toString(sumaVectores));
        


    }//GEN-LAST:event_GenerarActionPerformed

