    private int[] generateRandomVector(int size) {
        int[] vector = new int[size];
        Random rand = new Random();
        for (int i = 0; i < size; i++) {
            vector[i] = rand.nextInt(100); // Valores aleatorios entre 0 y 99
        }
        return vector;
    }

    private int findMax(int[] vector) {
        int max = Integer.MIN_VALUE;
        for (int value : vector) {
            if (value > max) {
                max = value;
            }
        }
        return max;
    }

    private int[] invertVector(int[] vector) {
        int[] inverted = new int[vector.length];
        for (int i = 0; i < vector.length; i++) {
            inverted[i] = vector[vector.length - 1 - i];
        }
        return inverted;
    }

    private int[] findEvenElements(int[] vector) {
        return Arrays.stream(vector).filter(value -> value % 2 == 0).toArray();
    }

    private int[] sumaVectores(int[] vectorN, int[] vectorM) {
        int minLength = Math.min(vectorN.length, vectorM.length);
        int[] sum = new int[minLength];
        for (int i = 0; i < minLength; i++) {
            sum[i] = vectorN[i] + vectorM[i];
        }
        return sum;
    }

